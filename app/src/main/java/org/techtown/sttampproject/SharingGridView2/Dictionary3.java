package org.techtown.sttampproject.SharingGridView2;

public class Dictionary3 { //사진 올리기 리사이클러뷰 에서의 딕셔너리


    private String image;
    private String posit;

    public Dictionary3(String image, String posit) {
        this.image = image;
        this.posit = posit;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getPosit() {
        return posit;
    }

    public void setPosit(String posit) {
        this.posit = posit;
    }
}

